// Required export functions
export const getLanguageCode = (language: string): string => {
  return languages[language as keyof typeof languages]?.code || "en-US"
}

export const getTranslation = (language: string, key: string): string => {
  return (
    translations[language as keyof typeof translations]?.[key as keyof typeof translations.english] ||
    translations.english[key as keyof typeof translations.english] ||
    key
  )
}

export const getLanguageFlag = (language: string): string => {
  return languages[language as keyof typeof languages]?.flag || "üåê"
}

export const getLanguageNativeName = (language: string): string => {
  return languages[language as keyof typeof languages]?.nativeName || language
}

// Hook for using translations in components
export const useTranslation = () => {
  const getCurrentLanguage = (): string => {
    if (typeof window !== "undefined") {
      return localStorage.getItem("app_language") || "english"
    }
    return "english"
  }

  const t = (key: string): string => {
    const currentLanguage = getCurrentLanguage()
    return getTranslation(currentLanguage, key)
  }

  const currentLanguage = getCurrentLanguage()

  return {
    t,
    currentLanguage,
    languages: Object.keys(languages),
    getLanguageCode: () => getLanguageCode(currentLanguage),
    getLanguageFlag: () => getLanguageFlag(currentLanguage),
    getLanguageNativeName: () => getLanguageNativeName(currentLanguage),
  }
}
